export const paragraphWrapper = (text) => {
  return `<p>${text}</p>`;
};

export const boldWrapper = (text) => {
  return `<b>${text}</b>`;
};

export const italicWrapper = (text) => {
  return `<i>${text}</i>`;
};

export const monospaceWrapper = (text) => {
  return `<tt>${text}</tt>`;
};

export const preformattedWrapper = (text) => {
  return `<pre>${text}</pre>`;
};

